<?php
/************************************************************************/
/******************* SEE IF A SESSION IS ALREADY STARTED ****************/
/************************************************************************/
function sessionStarted(){ if(isset($_SESSION)){ return true; }else{ return false; } }

/************************************************************************/
/******************* ASSURE ACTIVE SESSION ******************************/
/************************************************************************/
function assureSession(){ if(!sessionStarted()){ if(!headers_sent()){ session_start(); } } }
?>